// FUncion -- Retorna algo
function suma(n1, n2) {
    let res = n1 + n2;
    return res
}

operacion = suma(5, 4);
console.log(operacion)

// Procedimiento -- No retorna nada, solo ejecuta un procedimiento
function imprimir(mensaje) {
    console.log(mensaje);
}

msg = 'Hola, mundo';
imprimir(msg);

