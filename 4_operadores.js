// Aritméticos
/* 
    Suma                            +
    Resta                           -
    Multiplicación                  *
    División                        /
    Modulo (Residuo División)       %
    Potencia (Exponente)            **
*/
let x = 1
//  Incremento 
//      Pre-Incremento - El valor se aumenta en el momento de ejecutado el operador
++x;
//      Post-Incremento - El valor se aumentará la siguiente vez que se llame a la variable 
x++;
//  Decremento
//      Pre-Decremento - El valor se aumenta en el momento de ejecutado el operador
--x;
//      Post-Decremento - El valor se aumentará la siguiente vez que se llame a la variable 
x--;
// Asignación
//  Asignación                      =
//  Asignación compuesto
/*
        Suma                        +=
        Resta                       -=
        Multiplicación              *=
        División                    /=
        Modulo                      %=
        Exponente                   **=
*/

